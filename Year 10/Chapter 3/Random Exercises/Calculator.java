public class Calculator {
    public static void main(String [] Args) {
    	System.out.println("6" + " + " + "3" + " = " + (6 + 3));
    	System.out.println("6" + " - " + "3" + " = " + (6 - 3));
    	System.out.println("6" + " * " + "3" + " = " + (6 * 3));
    	System.out.println("6" + " / " + "3" + " = " + (6 / 3));
    }
}