public class tenName {
    public static void main(String[] args) {
    	int x = 0, y = 0;
    	while(x < 10){
    		x++;
    		System.out.println(x + ". " + "Chyna Sarmiento");
    	}
    	
    	System.out.print("\n");
    	
    	do{
    		y += 1;
    		System.out.println(y + ". " + "Chyna Sarmiento");
    	} while(y < 10);
    	
    	System.out.print("\n");
    	
    	for(int i = 1; i <= 10; i++){
    		System.out.println(i + ". " + "Chyna Sarmiento");
    	}
    }
}
